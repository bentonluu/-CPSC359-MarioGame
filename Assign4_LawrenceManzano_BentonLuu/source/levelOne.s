//This function draws the first level of Mario which includes, the hole on the ground and the goomba.
.globl levelOne
levelOne:
        push    {lr}

        b       blueLineCond

//Draws a blue square of size 64x64 onto the screen and the X position is incremented by 64 after.
blueBody:
        ldr     r9, =blueArea         //Draw the blue square.
        ldr     r0, [r9]
        ldr     r1, [r9, #4]
        ldr     r2, [r9, #8]
        ldr     r3, [r9, #12]
        bl      drawBlue

        ldr     r9, =blueArea         //Increment the blueArea label by 64 (X coordinates).
        ldr     r8, [r9]
        add     r8, #64
        str     r8, [r9]
        ldr     r8, [r9, #8]
        add     r8, #64
        str     r8, [r9, #8]

        ldr     r9, =blueCounter      //Increment the blueCounter.
        ldr     r8, [r9]
        add     r8, #1
        str     r8, [r9]

//Loops and draws a line of blue squares that cover up a portion of the intro screen.
blueBodyCond:
        ldr     r9, =blueCounter      //Loops back to blueBody if the blueCounter is less than 16.
        ldr     r8, [r9]
        cmp     r8, #16
        blt     blueBody

        ldr     r9, =blueArea         //Increment the blueArea by 64 (Y coordinates) and resets the X coordinates.
        mov     r8, #0
        str     r8, [r9]
        ldr     r8, [r9, #4]
        add     r8, #64
        str     r8, [r9, #4]
        mov     r8, #64
        str     r8, [r9, #8]
        ldr     r8, [r9, #12]
        add     r8, #64
        str     r8, [r9, #12]

        ldr     r9, =blueCounter      //Resets blueCounter back to its initial value.
        mov     r8, #0
        str     r8, [r9]

        ldr     r9, =blueLine         //Increments blueLine by 1.
        ldr     r8, [r9]
        add     r8, #1
        str     r8, [r9]

//Loops the blue line of squares until all intro screen is covered up.
blueLineCond:
        ldr     r9, =blueLine         //Loops back to clearCond if the clearLine is less than 10.
        ldr     r8, [r9]
        cmp     r8, #10
        blt     blueBodyCond

        ldr     r9, =blueLine         //Resets blueLine back to its initial value.
        mov     r8, #0
        str     r8, [r9]

        mov     r0, #512              //Draws the first section of the hole.
        mov     r1, #704
        mov     r2, #576
        mov     r3, #768
        bl      drawBlue

        mov     r0, #576              //Draws the second section of the hole.
        mov     r1, #704
        mov     r2, #640
        mov     r3, #768
        bl      drawBlue

        ldr     r9, =blueArea         //Resets blueArea back to its initial values.
        mov     r8, #0
        str     r8, [r9]
        mov     r8, #64
        str     r8, [r9, #4]
        mov     r8, #64
        str     r8, [r9, #8]
        mov     r8, #128
        str     r8, [r9, #12]

        ldr     r9, =killedGoomba     //Checks if the goomba is killed or not. If killed, goomba will not be redrawn.
        ldr     r8, [r9]
        cmp     r8, #1
        beq     end

        mov     r6, #704              //Draws the goomba.
        mov     r7, #46
        sub     r6, r7
        mov     r0, #272
        mov     r1, r6
        mov     r2, #320
        mov     r3, #704
        bl      drawGoomba

end:
        pop     {lr}
        bx 	lr			                 //Return to calling code.

//This function draws the blue square that is the same color as the background used to clear images from the screen.
//r0 the starting X position of the blue square.
//r1 the starting Y position of the blue square.
//r2 the ending X position of the blue square.
//r3 the ending Y position of the blue square.
.globl drawBlue
drawBlue:
        push    {r0-r3, lr}
        mov	r4,	r0			//Start X position of your picture
        mov     r9,     r0
	mov	r5,	r1
	ldr	r6,	=blue			//Address of the picture
	mov	r7,	r2			//Width of your picture
	mov	r8,	r3			//Height of your picture

//This function draws out the ascii image pixel by pixel onto the screen.
drawPictureLoop:
	mov	r0,	r4			//passing x for ro which is used by the Draw pixel function
	mov	r1,	r5			//passing y for r1 which is used by the Draw pixel formula

	ldrh	r2,	[r6],#2			//setting pixel color by loading it from the data section. We load hald word
	bl	DrawPixel
	add	r4,	#1			//increment x position
	cmp	r4,	r7			//compare with image with
	blt	drawPictureLoop
	mov	r4,	r9			//reset x
	add	r5,	#1			//increment Y
	cmp	r5,	r8			//compare y with image height
	blt	drawPictureLoop

        pop     {r0-r3, lr}
        bx 	lr			//return

