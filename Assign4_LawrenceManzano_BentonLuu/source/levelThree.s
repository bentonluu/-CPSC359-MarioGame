//This function draws the third level of Mario, which includes the two pipes.
.globl levelThree
levelThree:
        push    {lr}
        
        mov     r0, #256                //Draws the first pipe.
        mov     r1, #384
        bl      drawPipe

        mov     r0, #640                //Draws the second pipe.
        mov     r1, #768
        bl      drawPipe
            
        ldr     r9, =marioX             //Draws Mario standing on the ground at the leftmost side of the screen.
        ldr     r0, [r9]
        mov     r1, #640
        ldr     r2, [r9, #4]
        mov     r3, #704
        bl      drawMarioR
      
        pop     {lr}            
        bx      lr                      //Return to calling code.

//This function clears both third and the final level of Mario, drawing in the blue background.
.globl clearLevelThreeFinal
clearLevelThreeFinal:
        push    {lr}

        b       clearLineCond

//Draws a blue square of size 64x64 onto the screen and the X position is incremented by 64 after.
clearBody:
        ldr     r9, =clearPipeArea      //Draw the blue square.
        ldr     r0, [r9]
        ldr     r1, [r9, #4]
        ldr     r2, [r9, #8]
        ldr     r3, [r9, #12]
        bl      drawBlue

        ldr     r9, =clearPipeArea      //Increment the clearPipeArea label by 64 (X coordinates).
        ldr     r8, [r9]
        add     r8, #64
        str     r8, [r9]
        ldr     r8, [r9, #8]
        add     r8, #64
        str     r8, [r9, #8]

        ldr     r9, =clearCounter       //Increment the clearCounter.      
        ldr     r8, [r9]
        add     r8, #1  
        str     r8, [r9]

//Loops and draws a line of blue squares that cover up a portion the castle and pipe in their respective levels.        
clearCond:
        ldr     r9, =clearCounter       //Loops back to clearBody if the clearCounter is less than 6.      
        ldr     r8, [r9]
        cmp     r8, #16
        blt     clearBody

        ldr     r9, =clearPipeArea      //Increment the clearPipeArea by 64 (Y coordinates) and resets the X coordinates.
        mov     r8, #0
        str     r8, [r9]
        ldr     r8, [r9, #4]
        add     r8, #64
        str     r8, [r9, #4]
        mov     r8, #64
        str     r8, [r9, #8]
        ldr     r8, [r9, #12]
        add     r8, #64
        str     r8, [r9, #12]

        ldr     r9, =clearCounter       //Resets clearCounter back to its initial value.
        mov     r8, #0
        str     r8, [r9]    
        
        ldr     r9, =clearLineCounter   //Increments clearLineCounter by 1.
        ldr     r8, [r9]
        add     r8, #1
        str     r8, [r9]

//Loops the blue line of squares until all the castle and the pipe is covered up.
clearLineCond:
        ldr     r9, =clearLineCounter   //Loops back to clearCond if the clearLine is less than 4.
        ldr     r8, [r9]
        cmp     r8, #4
        blt     clearCond

        ldr     r9, =clearPipeArea      //Resets clearPipeArea back to its initial values.
        mov     r8, #0
        str     r8, [r9]
        mov     r8, #448
        str     r8, [r9, #4]
        mov     r8, #64
        str     r8, [r9, #8]
        mov     r8, #512
        str     r8, [r9, #12]

        ldr     r9, =clearLineCounter   //Resets clearLineCounter back to its initial value.
        mov     r8, #0
        str     r8, [r9]

        pop     {lr}
        bx      lr                      //Return to calling code.

//This function draws the pipe in the third level.
//Takes in 2 parameters:
//r0 the starting X position of the pipe.
//r1 the ending X position of the pipe.        
drawPipe:
        push    {r0-r1, lr}	
        
        mov	r4,	r0			//Start X position of your picture
        mov     r9,     r0
	mov	r5,	#512
	ldr	r6,	=pipe			//Address of the picture
	mov	r7,	r1			//Width of your picture
	mov	r8,	#704			//Height of your picture
        
drawPictureLoop:
	mov	r0,	r4			//passing x for ro which is used by the Draw pixel function
	mov	r1,	r5			//passing y for r1 which is used by the Draw pixel formula

	ldrh	r2,	[r6],#2			//setting pixel color by loading it from the data section. We load hald word
	bl	DrawPixel
	add	r4,	#1			//increment x position
	cmp	r4,	r7			//compare with image with
	blt	drawPictureLoop
	mov	r4,	r9			//reset x
	add	r5,	#1			//increment Y
	cmp	r5,	r8			//compare y with image height
	blt	drawPictureLoop
	
        pop     {r0-r1, lr}
        bx 	lr			//return

