endLevelTwo:
        mov     r6, #704            //Clears the goomba from the first level.
        mov     r7, #46
        sub     r6, r7
        mov     r0, #272
        mov     r1, r6
        mov     r2, #320
        mov     r3, #704
        bl      drawBlue

        ldr     r9, =marioX         //Redraws Mario on the leftmost side of the screen standing on the ground.
        ldr     r0, [r9]
        mov     r1, #640
        ldr     r2, [r9, #4]
        mov     r3, #704
        bl      drawMarioR

        pop     {lr}                //Return to calling code.
        bx      lr

//Clears the blocks on the second level from the screen with the blue background.
.globl clearLevelTwo
clearLevelTwo:
        push    {lr}

        b       clearCond

//Draws a blue square of size 64x64 onto the screen and the X position is incremented by 64 after.
clearBody:
        ldr     r9, =clearBlockArea       //Draw the blue square.
        ldr     r0, [r9]
        mov     r1, #448
        ldr     r2, [r9, #4]
        mov     r3, #512
        bl      drawBlue

        ldr     r9, =clearBlockArea       //Increment the clearBlockArea label by 64 (X coordinates).
        ldr     r8, [r9]
        add     r8, #64
        str     r8, [r9]
        ldr     r8, [r9, #4]
        add     r8, #64
        str     r8, [r9, #4]

        ldr     r9, =clearCounter         //Increment the clearCounter.
        ldr     r8, [r9]
        add     r8, #1
        str     r8, [r9]

//Loops and draws a line of blue squares that cover up the blocks on the second level.
clearCond:
        ldr     r9, =clearCounter         //Loops back to clearBody if the clearCounter is less than 16.
        ldr     r8, [r9]
        cmp     r8, #16
        blt     clearBody

        ldr     r9, =clearBlockArea       //Resets clearBlockArea back to its initial values.
        mov     r8, #0
        str     r8, [r9]
        mov     r8, #64
        str     r8, [r9, #4]

        ldr     r9, =clearCounter         //Resets clearCounter back to its initial value.
        mov     r8, #0
        str     r8, [r9]

        pop     {lr}
        bx      lr                        //Return to calling code.

