//This function draws the second level of Mario which includes, the 3 breakable bricks and 3 coin blocks.
.globl levelTwo
levelTwo:
        push    {lr}

        mov     r0, #512          //Fills in the first section of the hole of the first level of Mario.
        mov     r1, #704
        mov     r2, #576
        mov     r3, #768
        bl      drawBrick

        mov     r0, #576          //Fills in the second section of the hole of the first level of Mario.
        mov     r1, #704
        mov     r2, #640
        mov     r3, #768
        bl      drawBrick

//Draws the first breakable brick of the second level.
brickBreakBox:
        ldr     r9, =hitBrick       //Checks if the breakable brick has been hit or not. If hit will not redraw the breakable brick.
        ldr     r8, [r9]
        cmp     r8, #1
        beq     brickBreakBox2

        mov     r0, #512
        mov     r1, #576
        bl      drawBrickBreak

//Draws the second breakable brick of the second level.
brickBreakBox2:
        ldr     r9, =hitBrick       //Checks if the breakable brick has been hit or not. If hit will not redraw the breakable brick.
        ldr     r8, [r9, #4]
        cmp     r8, #1
        beq     brickBreakBox3

        mov     r0, #640
        mov     r1, #704
        bl      drawBrickBreak

//Draws the third breakable brick of the second level.
brickBreakBox3:
        ldr     r9, =hitBrick       //Checks if the breakable brick has been hit or not. If hit will not redraw the breakable brick.
        ldr     r8, [r9, #8]
        cmp     r8, #1
        beq     mysteryBox1

        mov     r0, #768
        mov     r1, #832
        bl      drawBrickBreak

//Draws the first coin block of the second level.
mysteryBox1:
        ldr     r9, =mysteryBox     //Checks if the coin block has been hit or not. If hit it will draw the hitMysteryBox image instead of the normal MysteryBox image.
        ldr     r8, [r9]
        cmp     r8, #1
        beq     mysteryHitBox1

        mov     r0, #256
        mov     r1, #320
        bl      drawMysteryBox

        b       mysteryBox2

//Draws the hitMysteryBox image if the first coin box has been hit.
mysteryHitBox1:
        mov     r0, #256
        mov     r1, #320
        bl      mysteryHit

//Draws the second coin block of the second level.
mysteryBox2:
        ldr     r9, =mysteryBox     //Checks if the coin block has been hit or not. If hit it will draw the hitMysteryBox image instead of the normal MysteryBox image.
        ldr     r8, [r9, #4]
        cmp     r8, #1
        beq     mysteryHitBox2

        mov     r0, #576
        mov     r1, #640
        bl      drawMysteryBox

        b       mysteryBox3

//Draws the hitMysteryBox image if the second coin box has been hit.
mysteryHitBox2:
        mov     r0, #576
        mov     r1, #640
        bl      mysteryHit

//Draws the third coin block of the second level.
mysteryBox3:
        ldr     r9, =mysteryBox     //Checks if the coin block has been hit or not. If hit it will draw the hitMysteryBox image instead of the normal MysteryBox image.
        ldr     r8, [r9, #4]
