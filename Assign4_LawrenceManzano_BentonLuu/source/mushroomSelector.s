//This function draws the mushroom selector used in the intro controls to differentiate between START and QUIT.
//Takes in 2 parameters:
//r0 the starting X position of the mushroom selector for the intro.
//r1 the ending X position of the mushroom selector for the intro.
.globl mushroomSelector
mushroomSelector:
        push {r0-r1, lr}
        mov	r4,	r0			//Start X position of your picture
        mov     r9,     r0
	mov	r5,	#480
	ldr	r6,	=pic			//Address of the picture
	mov	r7,	r1			//Width of your picture
	mov	r8,	#544			//Height of your picture

        b       drawPictureLoop

//This function draws the mushroom selector used in the pause controls to differentiate between RESTART and QUIT.
//Takes in 2 parameters:
//r0 the starting X position of the mushroom selector for the pause menu.
//r1 the ending X position of the mushroom selector for the pause menu.
.globl mushroomPause
mushroomPause:
        push {r0-r1, lr}
        mov	r4,	r0			//Start X position of your picture
        mov     r9, r0
	mov	r5,	#224
	ldr	r6,	=pic2			//Address of the picture
	mov	r7,	r1			//Width of your picture
	mov	r8,	#256			//Height of your picture

//This function draws out the ascii image pixel by pixel onto the screen.
drawPictureLoop:
	mov	r0,	r4			//passing x for ro which is used by the Draw pixel function
	mov	r1,	r5			//passing y for r1 which is used by the Draw pixel formula

	ldrh	r2,	[r6],#2			//setting pixel color by loading it from the data section. We load hald word
	bl	DrawPixel
	add	r4,	#1			//increment x position
	cmp	r4,	r7			//compare with image with
	blt	drawPictureLoop
	mov	r4,	r9			//reset x
	add	r5,	#1			//increment Y
	cmp	r5,	r8			//compare y with image height
	blt	drawPictureLoop
	pop {r0-r1, lr}
	bx 	lr			//return

